package ex10_For_Loop;

public class lab10_ex {
    public static void main(String[]args){
        for(int i=0;i<10;i++){
            System.out.println(i);
        }
        System.out.println("--------------------");
        for (int i=0;i<=10;i++){
            System.out.println(i);
        }
        System.out.println("--------------------");

        for(int i=1;i<=10;i++){
            System.out.println(i);
        }
    }
}
